$(function () {
    $(".enabled").click(function () {
        const enabled = $(this).is(":checked");
        const userId = $(this).closest('tr').attr("id");
        $.ajax({
            url: userAjaxUrl + userId + "/" + enabled,
            type: "PUT"
        }).done(function () {
            updateTable();
        });
    })
})